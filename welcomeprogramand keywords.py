import keyword

print("Demonstrating indentation, keywords, and variables in Python")
# Assigning a value to a variable
x = 7  # x is a variable
# Display the value of the variable
print("The value of x is:", x)
# Using a loop with indentation and a variable
for i in range(3):  # 'for' is a keyword, i is a variable
    print("Iteration number:", i)  # This line is indented



print("Python Keywords are:")
print(keyword.kwlist)
